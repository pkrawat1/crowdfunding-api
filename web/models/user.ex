defmodule CrowdfundingApi.User do
  use CrowdfundingApi.Web, :model

  schema "users" do

    timestamps()
  end

  @doc """
  Builds a changeset based on the `struct` and `params`.
  """
  def changeset(struct, params \\ %{}) do
    struct
    |> cast(params, [])
    |> validate_required([])
  end
end
